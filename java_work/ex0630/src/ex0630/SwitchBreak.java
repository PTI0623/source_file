package ex0630;

public class SwitchBreak {
	public static void main(String[] args) {
		int n =3;
		
		switch (n) {
		case 1:
			System.out.println("SIMPLE");
			break;
		case 2:
			System.out.println("FUNNY");
			break;
		case 3:
			System.out.println("THE BEST");
			break;

		default:
			break;
		}
	}
}
